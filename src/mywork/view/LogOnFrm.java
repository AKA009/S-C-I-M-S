    public LogOnFrm() {
        Font font = new Font("Dialog", Font.PLAIN, 12);
        java.util.Enumeration keys = UIManager.getDefaults().keys();
        while (keys.hasMoreElements()) {
            Object key = keys.nextElement();
            Object value = UIManager.get(key);
            if (value instanceof javax.swing.plaf.FontUIResource) {
                UIManager.put(key, font);
            }
        }
        initComponents();
        this.setLocationRelativeTo(null);
        this.jrb_student.setSelected(true);
        String s = SettingsUtil.GetServerIPFromSettings();
        if (s == null) {
            this.serverIPTxt.setText("");
        } else {
            this.serverIPTxt.setText(s);
        }

        StringBuilder IFCONFIG = new StringBuilder();
        try {
            for (Enumeration<NetworkInterface> en = NetworkInterface.getNetworkInterfaces(); en.hasMoreElements();) {
                NetworkInterface intf = en.nextElement();
                for (Enumeration<InetAddress> enumIpAddr = intf.getInetAddresses(); enumIpAddr.hasMoreElements();) {
                    InetAddress inetAddress = enumIpAddr.nextElement();
                    if(inetAddress.isLoopbackAddress())
                    {
                        this.localhostIPTxt.setText(inetAddress.getHostAddress().toString());
                    }
                    if (!inetAddress.isLoopbackAddress() && !inetAddress.isLinkLocalAddress() && inetAddress.isSiteLocalAddress()) {
                        IFCONFIG.append(inetAddress.getHostAddress().toString() + "\n");
                    }
                }
            }
        } catch (SocketException ex) {
        }
        this.localIPTxt.setText(IFCONFIG.toString());
    }

