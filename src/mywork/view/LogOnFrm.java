    private void jb_logOnActionPerformed(java.awt.event.ActionEvent evt) {
        String userName = userNameTxt.getText();
        String password = new String(passwordTxt.getPassword());
        String serverIP = serverIPTxt.getText();

        if (StringUtil.isEmpty(userName)) {
            JOptionPane.showMessageDialog(this, "用户名为空!");
            return;
        }
        if (StringUtil.isEmpty(password)) {
            JOptionPane.showMessageDialog(this, "密码为空!");
            return;
        }
        if (StringUtil.isEmpty(serverIP)) {
            JOptionPane.showMessageDialog(this, "服务端地址为空!");
            return;
        }

        SettingsUtil.serverIP = serverIP;
        SettingsUtil.SetServerIPToSettings(serverIP);

        Connection con = null;
        if (this.jrb_student.isSelected()) {
            Student student = new Student(Integer.parseInt(userName), password);
            try {
                con = dbUtil.getCon();
                currentStudent = logOnDao.login(con,
                        student);
                if (currentStudent != null) {
                    this.dispose();
                    new MainFrm_student().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "登录失败!");
                }
            } catch (Exception e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "登录失败!");
            } finally {
                try {
                    dbUtil.closeCon(con);
                } catch (Exception e) {
                    // TODO Auto-generated catch block
                    e.printStackTrace();
                }
            }
        } else if (this.jrb_student1.isSelected()) {
            Admin admin = new Admin(Integer.parseInt(userName), password);
            try {
                con = dbUtil.getCon();
                Admin currentAdmin = logOnDao.login(con, admin);
                if (currentAdmin != null) {
                    this.dispose();
                    new MainFrm_admin().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "�登录失败������������!");
                }
            } catch (Exception e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "登录失败!");
            } finally {
                try {
                    dbUtil.closeCon(con);
                } catch (Exception e) {
                    // TODO Auto-generated catch block
                    e.printStackTrace();
                }
            }
        }

    }

